// Arithmetic Operators:

// Addition (+)
console.log('Addition (+)');
const value_a = 20;
const value_b = 30;
console.log(value_a + value_b); // 50

// Subtraction (-)
console.log('Subtraction(-)');
const value_c = 50;
const value_d = 35;
console.log(value_c - value_d); // 15

// Multiplication (*)
console.log('Multiplication (*)');
const value_e = 45;
const value_f = 5;
console.log(value_e * value_f); // 225

// Division (/)
console.log('Division (/)');
const value_g = 45;
const value_h = 5;
console.log(value_g / value_h); // 9

// Modulus (%)
console.log('Modulus (%)');
const value_i = 2;
const value_j = 5;
console.log(value_i % value_j); // 2

// Increment (++)
console.log('Increment (++)');
var k = 5;
console.log(++k);
// Decrement (--)
console.log('Decrement (--)');
var l = 5;
console.log(--l);
